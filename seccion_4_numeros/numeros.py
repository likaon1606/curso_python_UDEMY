# division
dividir = 5 / 3 # con decimales
diviison = 5 // 3 # sin decimales, solo enteros

print(dividir)
print(diviison)

# modulo % osea el residuo

modulo = 5 % 3
print(modulo)

# pow o **, potencia de un numero
potencia = 5 ** 3
print(potencia)

potenciaPow = pow(5, 3) # entero
print(potenciaPow)

potenciaPowDecimal = pow(5, 3.) # flotante o decimal o tambien (5, 3.0)
print(potenciaPowDecimal)

