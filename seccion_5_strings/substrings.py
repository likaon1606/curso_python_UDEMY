# Sub Strings. Para acceder a las posiciones de un caracter
s = "Soy un estudiante muy bueno"
print(s[5]) # sexto caracter
print(s[-1]) # último carácter
print(s[-7]) # septimo elemento empezando por el final
print(s[4:7]) # quinto caracter hasta el septimo, "el último elemento no es incluido, por eso no es el 8 caratcer"
print(s[4:7]) # quinto caracter hasta el septimo
print(s[:7]) # del primero hasta el septimo
print(s[8:]) # del noveno al final
print(s[-10:]) # diez ultimos elementos
print(s[:-10]) # devuelve toda la frase, hasta las 10 ultimas frases
